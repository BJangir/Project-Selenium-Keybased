package com.xceleratorninja.action;

public class ActionPageMap {

	String page;
	String action_support;
	String action_on;
	String return_page;
	public String getPage() {
		return page;
	}
	public void setPage(String page) {
		this.page = page;
	}
	public String getAction_support() {
		return action_support;
	}
	public void setAction_support(String action_support) {
		this.action_support = action_support;
	}
	public String getAction_on() {
		return action_on;
	}
	public void setAction_on(String action_on) {
		this.action_on = action_on;
	}
	public String getReturn_page() {
		return return_page;
	}
	public void setReturn_page(String return_page) {
		this.return_page = return_page;
	}
	
	
	
}
